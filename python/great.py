# Realizzare un programma che dica se a è maggiore di b. Il programma deve dire se a è maggiore di b oppure se a non è maggiore di b

a= int(input("Indica il primo numero: ")) 
b= int(input("Indica il secondo numero: ")) 
if (a>b):
    print("Il numero a è maggiore di b") 
else:
    print("Il numero a non è maggiore di b")